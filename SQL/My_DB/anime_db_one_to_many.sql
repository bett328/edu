DROP DATABASE IF EXISTS animedb_one_to_many;
CREATE DATABASE IF NOT EXISTS animedb_one_to_many;
USE animedb_one_to_many;

-- Создаем таблицы со связью с id один ко многим
CREATE TABLE IF NOT EXISTS names (
    id INT PRIMARY KEY AUTO_INCREMENT,
    anime VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS rates (
    id INT PRIMARY KEY,
    episodes INT,
    votes INT,
    rate INT,
    FOREIGN KEY (id) REFERENCES names(id)
);

